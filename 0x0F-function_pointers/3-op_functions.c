#include "3-calc.h"

/**
 * op_add - calculate the sum of two integer
 * @a: first integer
 * @b: second integer
 *
 * Return: Always success
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference
 * @a: first integer
 * @b: second integer
 *
 * Return: Always success
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always success
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always success
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate remainder
 * @a: first integer
 * @b: second integer
 *
 * Return: Always success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
